#include <cstdio>

int main()
{
	int my_answer = 7;
	int lucky_number = 6;

	my_answer == lucky_number; //Equality
	my_answer != lucky_number; //Inequality
	my_answer <= lucky_number; //Less than or equal to
	my_answer >= lucky_number; //Greater than or equal to
	my_answer < lucky_number; //Less than
	my_answer > lucky_number; //Greater than

	return 0;
}